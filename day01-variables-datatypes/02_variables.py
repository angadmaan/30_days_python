# Variable is the name of the memory location where we store data. It is a symbolic name that represents a value in the program. In Python, we can create variables by assigning values to them using the assignment operator (=).

a = 10  # This line creates a variable named 'a' and assigns the value 10 to it.
b = "Hello"  # This line creates a variable named 'b' and assigns the string value "Hello" to it.
c = 3.14  # This line creates a variable named 'c' and assigns the float value 3.14 to it.

print(a)  # This line prints the value of variable 'a', which is 10.
print(b)  # This line prints the value of variable 'b', which is "Hello".
print(c)  # This line prints the value of variable 'c', which is 3.14.  

# Rules for naming variables in Python:
# 1. Variable names can contain letters, numbers, and underscores (_), but they cannot  start with a number.
# 2. Variable names are case-sensitive, meaning that 'myVariable' and 'myvariable' are considered different variables.
# 3. Variable names cannot be the same as Python keywords 
# 4. No special characters are allowed in variable names except for the underscore (_).
# 5. No whitespace is allowed in variable names. If you want to use multiple words in a variable name, you can use underscores to separate them (e.g., my_variable).ˀˀ