# Data types in Python

# Python has several built-in data types that allow us to store and manipulate different kinds of data. Some of the most commonly used data types in Python include:
# 1. Integer (int): Represents whole numbers, e.g., 5, -10, 0
# 2. Float (float): Represents decimal numbers, e.g., 3.14, -2.5, 0.0
# 3. String (str): Represents a sequence of characters, e.g., "Hello", 'Python'
# 4. Boolean (bool): Represents either True or False
# 5. List (list): Represents an ordered collection of items, e.g., [1, 2, 3], ['a', 'b', 'c']
# 6. Tuple (tuple): Represents an ordered, immutable collection of items, e.g., (1, 2, 3), ('a', 'b', 'c')
# 7. Dictionary (dict): Represents a collection of key-value pairs, e.g., {'name': 'Alice', 'age': 30}
# 8. Set (set): Represents an unordered collection of unique items, e.g., {1, 2, 3}, {'a', 'b', 'c'}

# Example of different data types in Python
# Integer
x = 10  # Integer
# Float
y = 3.14  # Float
# String
name = "Alice"  # String
# Boolean
is_student = True  # Boolean
# List
fruits = ["apple", "banana", "cherry"]  # List
# Tuple
coordinates = (10.0, 20.0)  # Tuple
# Dictionary
person = {"name": "Alice", "age": 30}  # Dictionary
# Set
unique_numbers = {1, 2, 3, 4, 5}    

print("Integer:", x)
print("Float:", y)
print("String:", name)
print("Boolean:", is_student)
print("List:", fruits)
print("Tuple:", coordinates)
print("Dictionary:", person)
print("Set:", unique_numbers)
